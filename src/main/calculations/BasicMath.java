package main.calculations;

public class BasicMath {

	public static void sum(int number1, int number2) {
		System.out.println(number1 + " + " + number2 + " = " + (number1 + number2));
	}
	
	public static void substraction(int number1, int number2) {
		System.out.println(number1 + " - " + number2 + " = " + (number1 - number2));
	}
}
